// Generate a random number between 0 (inclusive) and 1 (exclusive)
const randomValue = Math.random();
console.log(`Random Value: ${randomValue}`);

// Round a number to the nearest integer
const roundedValue = Math.round(5.7);
console.log(`Rounded Value: ${roundedValue}`);

// Get the absolute value of a number
const absoluteValue = Math.abs(-10);
console.log(`Absolute Value: ${absoluteValue}`);

// Round a number down to the nearest integer
const floorValue = Math.floor(5.7);
console.log(`Floor Value: ${floorValue}`);

// Round a number up to the nearest integer
const ceilValue = Math.ceil(5.2);
console.log(`Ceil Value: ${ceilValue}`);

// Find the minimum of two numbers
const minValue = Math.min(10, 5);
console.log(`Minimum Value: ${minValue}`);

// Find the maximum of two numbers
const maxValue = Math.max(10, 5);
console.log(`Maximum Value: ${maxValue}`);

// Calculate the result of raising a number to a power
const powerValue = Math.pow(2, 3); // 2^3 = 8
console.log(`Power Value: ${powerValue}`);

// Calculate the square root of a number
const sqrtValue = Math.sqrt(25);
console.log(`Square Root Value: ${sqrtValue}`);
